#[cfg(test)]
mod tests {
    use super::*;
    use crate::parser::Parser;
    use crate::codegen::generate_module;

    #[test]
    fn test_scalediv_compile() {
        let ir = r#"
define i32 @ScaleDiv(i32 %0, i32 %1) {
  %3 = icmp eq i32 %1, 0
  br i1 %3, label %ret_zero, label %do_div

ret_zero:
  ret i32 0

do_div:
  %5 = sdiv i32 %0, %1
  ret i32 %5
}
"#;
        let mut p = Parser::new(ir);
        let module = p.parse_module().unwrap();
        assert_eq!(module.functions.len(), 1);
        assert_eq!(module.functions[0].name, "ScaleDiv");
        assert_eq!(module.functions[0].blocks.len(), 3);

        let cg = generate_module(&module).unwrap();
        assert_eq!(cg.functions.len(), 1);
        assert!(!cg.functions[0].1.is_empty(), "codegen produced no bytecode");
        assert_eq!(cg.functions[0].1.len() % 4, 0, "bytecode not word-aligned");
        eprintln!("ScaleDiv: {} bytes ({} instructions)",
            cg.functions[0].1.len(), cg.functions[0].1.len() / 4);
    }

    #[test]
    fn test_global_array() {
        let ir = r#"
@tilemap = dso_local global [4096 x i8] zeroinitializer, align 16

define i32 @read_tile(i32 %0) {
  %2 = sext i32 %0 to i64
  %3 = getelementptr inbounds [4096 x i8], ptr @tilemap, i64 0, i64 %2
  %4 = load i8, ptr %3, align 1
  %5 = zext i8 %4 to i32
  ret i32 %5
}
"#;
        let mut p = Parser::new(ir);
        let module = p.parse_module().unwrap();
        assert_eq!(module.globals.len(), 1);
        assert_eq!(module.globals[0].name, "tilemap");

        let cg = generate_module(&module).unwrap();
        assert!(!cg.data.is_empty(), "data section should contain tilemap");
        eprintln!("tilemap data: {} bytes", cg.data.len());
    }

    #[test]
    fn test_multi_function_compile() {
        let ir = r#"
define i32 @add(i32 %0, i32 %1) {
  %3 = add i32 %0, %1
  ret i32 %3
}

define i32 @mul(i32 %0, i32 %1) {
  %3 = mul i32 %0, %1
  ret i32 %3
}

define i32 @madd(i32 %0, i32 %1, i32 %2) {
  %4 = mul i32 %0, %1
  %5 = add i32 %4, %2
  ret i32 %5
}
"#;
        let mut p = Parser::new(ir);
        let module = p.parse_module().unwrap();
        assert_eq!(module.functions.len(), 3);

        let cg = generate_module(&module).unwrap();
        assert_eq!(cg.functions.len(), 3);

        let tobj = build_tobj(&cg).unwrap();
        assert!(!tobj.is_empty());
        eprintln!("multi-function → {} bytes .tobj", tobj.len());
    }
}